package com.ziyuan.web.manager.domain;

public class CountTodayBean {
	
	private int count;
	
	private double distributor_sum;
	
	private double supplier_sum;

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public double getDistributor_sum() {
		return distributor_sum;
	}

	public void setDistributor_sum(double distributor_sum) {
		this.distributor_sum = distributor_sum;
	}

	public double getSupplier_sum() {
		return supplier_sum;
	}

	public void setSupplier_sum(double supplier_sum) {
		this.supplier_sum = supplier_sum;
	}
	

}
